package Minesweeper;

/**
 * Minesweeper
 *
 * Position class:
 * Defines a simple x and y coordinate pair.
 *
 * @author Peter Mitchell
 * @version 2021.1
 */
public class Position {
    public int x;
    public int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
